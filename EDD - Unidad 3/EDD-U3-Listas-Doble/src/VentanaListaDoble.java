    private void mostrarLista(){
        //comprueba si la lista esta vacia si lo esta, imprime en pantalla "Lista vacia" y termina el metodo.
        if(listaD.estaVacia()){
            jTextArea1.setText("Lista vacia");
            return;
        }
        //se crea un nodo auxiliar y se asigna en donde esta ini.
        NodoDoble temp = listaD.getIni();
        //se crea una variable de tipo string para guardar la informacion del nodo (el dato).
        String cad = "";
        
        do{
            //se concatena el dato de nuestro nodo temporal.
            cad += temp.getDato() + "\n";
            //se brinca al siguiente nodo al darle la direccion de memoria del nodo siguiente.
            temp = temp.getSig();
            /**mientras que temp sea diferente de null (vacio), el ciclo se vuelve a repetir.
             * es decir, si temp es null, significa que ya no hay mas nodos.
             * esto debido a que, como el apuntador del ultimo nodo de la lista esta aterrizado a null,
             * temp se pasa a null.
             */
        }while(temp != null);
        jTextArea1.setText(cad);
    }
    
