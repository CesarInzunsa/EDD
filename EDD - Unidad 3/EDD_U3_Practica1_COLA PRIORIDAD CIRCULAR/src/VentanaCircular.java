        private void mostrar(){
            String cad = "";
        
            if(vector.estaColaSimpleVacia()){
                jTextArea1.setText("Cola vacia");
            }else{
                //mostrar modo normal
                if(vector.getIni()<=vector.getFin()){
                    for(int i=vector.getIni(); i<=vector.getFin(); i++){
                        cad = "["+i+"] = "+vector.getVector(i).getDato()+"\n" + cad;
                    }
                    jTextArea1.setText(cad);
                }else{
                    //mostrar modo circular
                    for(int i=vector.getIni(); i<vector.getTama単o(); i++){
                        cad = "["+i+"] = "+vector.getVector(i).getDato()+"\n" + cad;
                    }
                    jTextArea1.setText(cad);
                
                    for(int i=0; i<=vector.getFin(); i++){
                        cad = "["+i+"] = "+vector.getVector(i).getDato()+"\n" + cad;
                    }
                    jTextArea1.setText(cad);
            }   
        }   
            
        }//fin mostrar
        
