    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // INSERTAR
        char dato = jTextField1.getText().charAt(0);
        int nivel = Integer.parseInt(jSpinner1.getValue().toString());
        
        objeto = new Prioridad(dato, nivel);
        boolean respuesta = vector.insertar(objeto);
        
        if(respuesta){
            mostrar();
            jTextField1.setText("");
        }else{
            JOptionPane.showMessageDialog(this, "ERROR! cola llena");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

