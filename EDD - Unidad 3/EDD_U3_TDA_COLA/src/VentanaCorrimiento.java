    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // ELIMINAR
        boolean respuesta = objeto.eliminar();
        if(respuesta){
            mostrarColaConCorrimiento();
        }else{
            JOptionPane.showMessageDialog(this, "ERROR! cola vacia");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

        private void mostrarColaConCorrimiento(){
        String cad = "";
        
        if(objeto.estaColaSimpleVacia()){
            jTextArea1.setText("Cola vacia");
        }else{
            for(int i=objeto.getIni(); i<=objeto.getFin(); i++){
                cad = "["+i+"] = "+objeto.getVector(i)+"\n" + cad;
            }
            jTextArea1.setText(cad);
        }
    }
        
